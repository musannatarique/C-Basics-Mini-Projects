#include <stdio.h>

int main()
{
    int a = 10;
    int b = 4;
    int result;

    // Addition
    result = a + b;
    printf("Addition: %d\n", result);

    // Subtraction
    result = a - b;
    printf("Subtraction: %d\n", result);

    // Multiplication
    result = a * b;
    printf("Multiplication: %d\n", result);

    // Division
    result = a / b;
    printf("Division: %d\n", result);

    // Modulo
    result = a % b;
    printf("Modulo: %d\n", result);

    return 0;
}